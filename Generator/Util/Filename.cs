        public static string GenFilename(int hash)
        {
            int adjIndex = (hash >> 23) & 0x1FF;
            int nounIndex = (hash >> 16) & 0x7F;

            string adj = adjectives[adjIndex];
            string noun = nouns[nounIndex];
            string threeChar = sixteenBitTo3Char((UInt16)(hash & 0xFFFF));

            return adj + noun + "_" + threeChar;
        }

        private static readonly string chars = "0123456789abcDefghiJkLmNopQrstuVwxyzABEHR";

        private static string sixteenBitTo3Char(UInt16 number)
        {
            List<char> charList = new List<char>(3);
            int currNum = number;

            for (int i = 0; i < 3; i++)
            {
                int charIndex = currNum % chars.Length;
                charList.Add(chars[charIndex]);
                currNum = (currNum - charIndex) / chars.Length;
            }

            charList.Reverse();
            return String.Join("", charList);
        }
